DATA SEGMENT
    INFOR DB "1+2+...+$"
    EQUAL DB "=$"
    N DB 6 DUP(?)
    SUM DB 6 DUP(?) 
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
START:
    MOV AX,DATA
  	MOV DS,AX
	MOV AX,0
    MOV BX,1
LOOP1:
	ADD AX,BX
	CMP AX,60000
	JA SHOW
	INC BX
	JMP LOOP1
SHOW:
	PUSH AX
	MOV DX,OFFSET INFOR
	MOV AH,09H
	INT 21H
	MOV AX,BX
	MOV CX,10
	MOV BX,OFFSET N+5
	MOV BYTE PTR[BX],'$'
	LOOP2:
		MOV DX,0
		DIV CX
		ADD DL,30H
		DEC BX
		MOV[BX],DL
		OR AX,AX
		JNZ LOOP2
	MOV DX,BX
	MOV AH,09H
	INT 21H
	MOV DX,OFFSET EQUAL
	MOV AH,09H
	INT 21H
	POP AX
	MOV BX,OFFSET SUM+5
	MOV BYTE PTR[BX],'$'
	LOOP3:
		MOV DX,0
		DIV CX
		ADD DL,30H
		DEC BX
		MOV [BX],DL
		OR AX,AX
		JNZ LOOP3
	MOV DX,BX
	MOV AH,09H
	INT 21H
	MOV AH,4CH
	INT 21H
CODE ENDS
	END START

